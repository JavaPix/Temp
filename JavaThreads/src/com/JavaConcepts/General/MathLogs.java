package com.JavaConcepts.General;

public class MathLogs {

	public static void main(String[] args) {
		double d1 = 10.101;
		double d2 = 2.75;
		
		System.out.println(Math.E);
		System.out.println("PI: "+Math.PI);
		System.out.println("Power: "+Math.pow(d1, d2));
		System.out.println("Square root: "+Math.sqrt(d2));
	}
}
