package com.JavaConcepts.General;

public class MathFunctions {

	public static void main(String[] args) {
		double d1 = -1021.102;
		double d2 = 40.11;
		int i = 10, j = 20;
		System.out.println("Absolute: "+Math.abs(d1));
		System.out.println("Ceil : "+Math.ceil(d2));
		System.out.println("floor: "+Math.floor(d1));
		System.out.println("Rint: "+Math.rint(i));
		System.out.println("Max: "+Math.max(d1, d2));
		System.out.println("Min: "+Math.min(d1, d2));
		
	}
}
